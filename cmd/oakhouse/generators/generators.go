package generators

// This package contains code generators for the Go To Oakhouse framework.
// Each generator type is organized into separate files for better maintainability:
//
// - project.go: Project creation and initialization
// - resource.go: Complete CRUD resource generation
// - model.go: Database model generation
// - repository.go: Repository layer generation
// - service.go: Service layer generation
// - handler.go: HTTP handler generation
// - dto.go: Data Transfer Object generation
// - route.go: Route and endpoint generation
// - scope.go: GORM scope generation
// - middleware.go: HTTP middleware generation
// - server.go: Development server and build utilities
//
